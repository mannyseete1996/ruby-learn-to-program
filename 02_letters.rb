# String Arithmetic

# I like apple pie.
puts 'I like ' + 'apple pie.'

# blink blink blink blink
puts 'blink ' * 4

# 12 vs '12'

# 24
puts 12 + 12

# 1212
puts '12' + '12'

# 12 + 12
puts '12 + 12'

# 10
puts 2 * 5

# 22222
puts '2' * 5

# 2 * 5
puts '2 * 5'

# You're swell!
puts 'You\'re swell!'

# backslash at the end of a string:  \
puts 'backslash at the end of a string:  \\'

# up\down
puts 'up\\down'

# up\down
puts 'up\down'
